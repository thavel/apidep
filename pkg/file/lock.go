package file

import (
	"crypto/sha256"
	"encoding/base64"
	"fmt"
	"os"
	"sort"

	"gopkg.in/yaml.v3"
)

const (
	header = "# Generated by apidep. DO NOT EDIT.\n"
)

// File: api.lock.yml
type ApiLock struct {
	Version int       `yaml:"version"`
	Deps    []DepLock `yaml:"deps"`
}

func (l *ApiLock) Upsert(entry DepLock) {
	for i, d := range l.Deps {
		if d.Source == entry.Source {
			l.Deps[i] = entry
			return
		}
	}
	l.Deps = append(l.Deps, entry)
}

func (l *ApiLock) Get(source string) (*DepLock, bool) {
	for i := range l.Deps {
		if l.Deps[i].Source == source {
			return &l.Deps[i], true
		}
	}
	return nil, false
}

type DepLock struct {
	Source string `yaml:"source"`
	Commit string `yaml:"commit,omitempty"`
	Hash   string `yaml:"hash"`
}

func NewLock() *ApiLock {
	return &ApiLock{Version: version}
}

func ReadLock(filePath string) (*ApiLock, error) {
	data, err := os.ReadFile(filePath)
	if os.IsNotExist(err) {
		return NewLock(), nil
	}
	if err != nil {
		return nil, fmt.Errorf("read lock %s: %w", filePath, err)
	}
	var lock ApiLock
	if err := yaml.Unmarshal(data, &lock); err != nil {
		return nil, fmt.Errorf("parse lock %s: %w", filePath, err)
	}
	return &lock, nil
}

func WriteLock(filePath string, lock *ApiLock) error {
	data, err := serialize(lock)
	if err != nil {
		return fmt.Errorf("serialize lock: %w", err)
	}
	content := append([]byte(header), data...)
	if err := os.WriteFile(filePath, content, filePerm); err != nil {
		return fmt.Errorf("write lock %s: %w", filePath, err)
	}
	return nil
}

func Hash(refs map[string][]byte) string {
	paths := make([]string, 0, len(refs))
	for p := range refs {
		paths = append(paths, p)
	}
	sort.Strings(paths)

	hash := sha256.New()
	for _, p := range paths {
		h := sha256.Sum256(refs[p])
		fmt.Fprintf(hash, "%s:%x\n", p, h)
	}
	return prefix(version) + ":" + base64.StdEncoding.EncodeToString(hash.Sum(nil))
}

func prefix(version int) string {
	return fmt.Sprintf("h%d", version)
}
